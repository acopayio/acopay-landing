/** Partials may override any leaf with any string (not English literals). */
type DeepPartialMessages = {
  [K in keyof Messages]?: {
    [P in keyof Messages[K]]?: string;
  };
};

function deepMerge(base: Messages, over?: DeepPartialMessages): Messages {
  if (!over) return base;
  const out = { ...base } as unknown as Record<string, Record<string, string>>;
  for (const section of Object.keys(over) as (keyof Messages)[]) {
    const oSec = over[section];
    if (!oSec) continue;
    out[section as string] = {
      ...(base[section] as Record<string, string>),
      ...(oSec as Record<string, string>),
    };
  }
  return out as unknown as Messages;
}

